public class DataTypes
{
    public static void main(String args[])
    {
        //For Int Data Tyoe;
        int a,b,c;
        a=20;
        b=50;
        c=a+b;
        System.out.println("c (int): "+c);

        //For Long Data Type;
        long d,e,f;
        d=20L;
        e=30L;
        f=a+b;
        System.out.println("f (long): "+f);

        //For Double Data Type;
        double g,h,i;
        g=99d;
        h=100d;
        i=g+h;
        System.out.println("i (double): "+i);

        //For Float Data Type;
        float j,k,l;
        j=45.61f;
        k=67.99f;
        l=j+k;
        System.out.println("l (float): "+l);

        

    }
}